use std::fs;
use std::io::{Error, ErrorKind};
use std::path::{Path, PathBuf};

use jikji_core::{LEGACY_ROOT_AGENT_MAP, ROOT_AGENT_MAP, Result, io_error, json_error};
use serde_json::Value;

use crate::models::EvalCase;

const AGENT_RULES_BEGIN: &str = "<!-- BEGIN JIKJI ROUTING (auto-generated by `jikji prepare`; do not edit inside this block) -->";
const AGENT_RULES_END: &str = "<!-- END JIKJI ROUTING -->";
const AGENT_RULE_FILES: &[&str] = &["AGENTS.md", "CLAUDE.md", ".cursorrules"];

pub(crate) fn corpus_files(root: &Path) -> Result<Vec<String>> {
    let mut out = Vec::new();
    collect_files(root, root, &mut out)?;
    out.sort();
    Ok(out)
}

pub(crate) fn assert_no_leak(root: &Path, eval_set: &Path, allow_leak: bool) -> Result<()> {
    if allow_leak {
        return Ok(());
    }
    if let Ok(relative) = eval_set.strip_prefix(root)
        && !relative.starts_with(".jikji")
    {
        return Err(invalid_input(format!(
            "eval set is inside benchmark root: {}",
            eval_set.display()
        )));
    }
    Ok(())
}

pub(crate) fn read_eval_cases(path: &Path) -> Result<Vec<EvalCase>> {
    let text = fs::read_to_string(path).map_err(|source| io_error(path, source))?;
    text.lines()
        .filter(|line| !line.trim().is_empty())
        .map(|line| serde_json::from_str(line).map_err(|source| json_error(path, source)))
        .collect()
}

pub(crate) fn read_json(path: &Path) -> Result<Value> {
    let text = fs::read_to_string(path).map_err(|source| io_error(path, source))?;
    serde_json::from_str(&text).map_err(|source| json_error(path, source))
}

pub(crate) fn write_json(path: &Path, value: &Value) -> Result<()> {
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent).map_err(|source| io_error(parent, source))?;
    }
    let text = serde_json::to_string_pretty(value).map_err(|source| json_error(path, source))?;
    fs::write(path, format!("{text}\n")).map_err(|source| io_error(path, source))
}

pub(crate) fn write_jsonl(path: &Path, cases: &[EvalCase]) -> Result<()> {
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent).map_err(|source| io_error(parent, source))?;
    }
    let mut text = String::new();
    for case in cases {
        let line = serde_json::to_string(case).map_err(|source| json_error(path, source))?;
        text.push_str(&line);
        text.push('\n');
    }
    fs::write(path, text).map_err(|source| io_error(path, source))
}

pub(crate) fn canonical_dir(path: &Path) -> Result<PathBuf> {
    let clean = path
        .canonicalize()
        .map_err(|source| io_error(path, source))?;
    if clean.is_dir() {
        Ok(clean)
    } else {
        Err(invalid_input(format!(
            "path is not a directory: {}",
            clean.display()
        )))
    }
}

pub(crate) fn absolute_path(path: &Path) -> Result<PathBuf> {
    if path.is_absolute() {
        return Ok(path.to_path_buf());
    }
    let cwd = std::env::current_dir().map_err(|source| io_error(".", source))?;
    Ok(cwd.join(path))
}

pub(crate) fn invalid_input(message: impl Into<String>) -> jikji_core::JikjiError {
    io_error(
        "<bench>",
        Error::new(ErrorKind::InvalidInput, message.into()),
    )
}

fn collect_files(root: &Path, dir: &Path, out: &mut Vec<String>) -> Result<()> {
    for entry in fs::read_dir(dir).map_err(|source| io_error(dir, source))? {
        let entry = entry.map_err(|source| io_error(dir, source))?;
        let path = entry.path();
        let name = entry.file_name();
        let name_text = name.to_string_lossy();
        if matches!(
            name_text.as_ref(),
            ".jikji" | ROOT_AGENT_MAP | LEGACY_ROOT_AGENT_MAP
        ) {
            continue;
        }
        if path.is_dir() {
            collect_files(root, &path, out)?;
        } else if path.is_file() && !is_pure_generated_agent_rule(&path, &name_text)? {
            let relative = path
                .strip_prefix(root)
                .map_err(|_| invalid_input("fixture path escaped root"))?
                .to_string_lossy()
                .replace('\\', "/");
            out.push(relative);
        }
    }
    Ok(())
}

fn is_pure_generated_agent_rule(path: &Path, name: &str) -> Result<bool> {
    if !AGENT_RULE_FILES.contains(&name) {
        return Ok(false);
    }
    let text = fs::read_to_string(path).map_err(|source| io_error(path, source))?;
    let trimmed = text.trim();
    Ok(trimmed.starts_with(AGENT_RULES_BEGIN) && trimmed.ends_with(AGENT_RULES_END))
}
